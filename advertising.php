<main class="content">

  <div class="policy-card">
    <h2>Purpose of This Code</h2>
    <p>
      TrustFund is built to help people promote products, services, skills and opportunities in a trusted local marketplace. 
      This Code of Advertising Practice explains the standards that sellers and users must follow when posting listings, promotions or public content on the platform.
    </p>
  </div>

  <div class="policy-card">
    <h2>Honest and Accurate Listings</h2>
    <p>
      All advertisements and listings must be truthful, clear and not misleading. Sellers must accurately describe the item, service, skill or opportunity being offered.
    </p>
    <ul>
      <li>Prices must be clearly stated where possible.</li>
      <li>Images should represent the actual product or service.</li>
      <li>Descriptions should not exaggerate quality, condition or availability.</li>
      <li>Hidden costs should be disclosed before a buyer commits.</li>
    </ul>
  </div>

  <div class="policy-card">
    <h2>Prohibited Advertising</h2>
    <p>
      TrustFund does not allow advertising that is unsafe, illegal, harmful or misleading.
    </p>
    <ul>
      <li>Fake products, scams or fraudulent offers.</li>
      <li>Illegal goods or services.</li>
      <li>False job opportunities, fake learnerships or misleading internships.</li>
      <li>Content that promotes hate, harassment or discrimination.</li>
      <li>Advertising that targets users unfairly or pressures them into unsafe decisions.</li>
    </ul>
  </div>

  <div class="policy-card">
    <h2>Community Respect</h2>
    <p>
      All advertising on TrustFund must respect users and the broader community. Listings must not include offensive language, harmful stereotypes or content that exploits vulnerable people.
    </p>
  </div>

  <div class="policy-card">
    <h2>Images and Content Ownership</h2>
    <p>
      Users should only upload images, logos, product photos or written content they own or have permission to use. Using stolen images or copying another seller’s content may result in removal of the listing.
    </p>
  </div>

  <div class="policy-card">
    <h2>Opportunities and Job Listings</h2>
    <p>
      Job, gig, internship, bootcamp and learnership posts must be clear and genuine. Users must not post opportunities that ask for unnecessary upfront payments, false application fees or misleading promises.
    </p>
    <div class="highlight">
      TrustFund may remove opportunity listings that appear suspicious, unclear or harmful to users.
    </div>
  </div>

  <div class="policy-card">
    <h2>Reviews and Reputation</h2>
    <p>
      Users may not create fake reviews, manipulate ratings or misrepresent customer feedback. Trust and reputation are important parts of the TrustFund community.
    </p>
  </div>

  <div class="policy-card">
    <h2>Enforcement</h2>
    <p>
      TrustFund may review, edit, hide or remove listings that do not follow this Code. Repeated violations may result in account restrictions or removal from the platform.
    </p>
  </div>

  <div class="policy-card">
    <h2>Reporting Concerns</h2>
    <p>
      Users are encouraged to report misleading, unsafe or suspicious advertisements. Reports help keep the marketplace safe and reliable for everyone.
    </p>
    <div class="highlight">
      Contact: <strong>support@trustfund.co.za</strong>
    </div>
